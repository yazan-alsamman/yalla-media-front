export function relativeTimeLabel(_language: Language, value: string) {
  const minuteMatch = value.match(/^(\d+)\s+min\s+ago$/i)
  if (minuteMatch) return `منذ ${minuteMatch[1]} دقيقة`

  const hourMatch = value.match(/^(\d+)\s+hours?\s+ago$/i)
  if (hourMatch) return `منذ ${hourMatch[1]} ساعة`

  const dayMatch = value.match(/^(\d+)\s+days?\s+ago$/i)
  if (dayMatch) return `منذ ${dayMatch[1]} يوم`

  const monthMatch = value.match(/^(\d+)\s+months?\s+ago$/i)
  if (monthMatch) return `منذ ${monthMatch[1]} شهر`

  return value
}

